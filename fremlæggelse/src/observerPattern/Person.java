package observerPattern;

public class Person 
{

		private String name;
		private String age;
		private String cpr;
		
		public Person()
		{
			
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public String getAge() {
			return age;
		}

		public void setAge(String age) {
			this.age = age;
		}

		public String getCpr() {
			return cpr;
		}

		public void setCpr(String cpr) {
			this.cpr = cpr;
		}
}
